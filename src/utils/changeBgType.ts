export const changeBGColor = (name: string) => {
   if (name === 'grass') {
     return 'bg-grass'
   } else if (name === 'poison') {
     return 'bg-poison'
   }else if (name === 'fire') {
     return 'bg-fire'
   }else if (name === 'flying') {
     return 'bg-flying'
   }else if (name === 'water') {
     return 'bg-water'
   }else if (name === 'normal') {
     return 'bg-normal'
   }else if (name === 'electric') {
     return 'bg-electric'
   }else if (name === 'ground') {
     return 'bg-ground'
   }else if (name === 'fighting') {
     return 'bg-fighting'
   }else if (name === 'psychic') {
     return 'bg-psychic'
   }else if (name === 'rock') {
     return 'bg-rock'
   }else if (name === 'steel') {
     return 'bg-steel'
   }else if (name === 'ice') {
     return 'bg-ice'
   }else if (name === 'ghost') {
     return 'bg-ghost'
   }else if (name === 'dragon') {
     return 'bg-dragon'
   }else if (name === 'bug') {
     return 'bg-bug'
   }else if (name === 'fairy') {
     return 'bg-fairy'
   }else {
     return 'bg-dark'
   }
 }